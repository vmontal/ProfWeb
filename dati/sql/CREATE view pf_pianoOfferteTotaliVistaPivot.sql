FROM     (SELECT idRS, idSede, idGiorno, giorno, dataOfferta, idOra, idStatusOfferta
                  FROM      pf_pianoOfferteVista
                  WHERE     idStatusOfferta = 2) AS sourceTable PIVOT (COUNT(idStatusOfferta) FOR idOra IN ([1], [2], [3], [4], [5])) AS pivotTable
